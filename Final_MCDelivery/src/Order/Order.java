package Order;

public class Order {
	private int order_no;
	private int order_price;
	private String order_date;
	private int order_memberNo;
	private int order_paymethodNo;
	private int order_branchNo;
	private int order_memberaddressNo;
	public int getOrder_no() {
		return order_no;
	}
	public void setOrder_no(int order_no) {
		this.order_no = order_no;
	}
	public int getOrder_price() {
		return order_price;
	}
	public void setOrder_price(int order_price) {
		this.order_price = order_price;
	}
	public String getOrder_date() {
		return order_date;
	}
	public void setOrder_date(String order_date) {
		this.order_date = order_date;
	}
	public int getOrder_memberNo() {
		return order_memberNo;
	}
	public void setOrder_memberNo(int order_memberNo) {
		this.order_memberNo = order_memberNo;
	}
	public int getOrder_paymethodNo() {
		return order_paymethodNo;
	}
	public void setOrder_paymethodNo(int order_paymethodNo) {
		this.order_paymethodNo = order_paymethodNo;
	}
	public int getOrder_branchNo() {
		return order_branchNo;
	}
	public void setOrder_branchNo(int order_branchNo) {
		this.order_branchNo = order_branchNo;
	}
	public int getOrder_memberaddressNo() {
		return order_memberaddressNo;
	}
	public void setOrder_memberaddressNo(int order_memberaddressNo) {
		this.order_memberaddressNo = order_memberaddressNo;
	}
}
